// Operadores de Atribuição
/* 
    =       -> Atribuição
    +=      -> Adição
    -=      -> Subtração
    /=      -> divisão
    *=      -> Multiplicação
    %=      -> Resto
    >       -> maior
    >=      -> maior ou igual
    <       -> menor
    <=      -> menor ou igual

*/

let atribuicao = 10
let adicao = 5
adicao +=2
let subtracao = 7
subtracao -=2
let divisao = 8
divisao /=2
let multiplicacao = 9
multiplicacao *= 2
let resto = 10
resto %= 2

let firstNumber = 10
let secondNumber = 20

console.log(atribuicao, adicao, subtracao, divisao, multiplicacao, resto );
console.log(firstNumber >= secondNumber);