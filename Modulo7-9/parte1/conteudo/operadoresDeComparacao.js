// Operadores de Comparacao

/*

==      -> Igual ele compara o visualViewport, mas não compara o tipo de valor
===     -> Totalmente igual ele verifica o tipo de dado!
!=      -> Diferente ele compara o valor, mas não compara o tipo de valor
!==     -> Toltamente diferente ele verifica o tipo do dado!

>       -> maior
>=      -> maior ou igual
<       -> menor
<=      -> menor ou igual

*/

const firstPerson = "João"
const secondPerson = "Maria"

const firstNumber = 23
const secondNumber = "23"

console.log(firstPerson === secondPerson)
console.log(firstPerson !== secondPerson)
console.log(firstNumber === secondNumber)
console.log(firstNumber !== secondNumber)
console.log(firstNumber >= secondNumber)